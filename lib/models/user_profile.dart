class UserProfile {
  final int age;
  final String gender;
  final double height;
  final double weight;
  final double bmi;
  final double bodyFatRate;

  UserProfile({
    required this.age,
    required this.gender,
    required this.height,
    required this.weight,
    required this.bmi,
    required this.bodyFatRate,
  });
}
